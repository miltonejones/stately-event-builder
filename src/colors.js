function getContrastColor(rgb) {
  const p = h => h / 255;
  // Calculate the luminance of the color
  const luminance = (0.2126 * p(rgb.r)) + (0.7152 * p(rgb.g)) + (0.0722 * p(rgb.b));
// alert(luminance)
  // Return black or white based on the luminance
  return (luminance > 0.5) ? '#000' : '#fff';
}
export const opposite = (hue) => {
  if (!htmlColorsToRGB[hue]) return "lime";
  const [ r, g, b] = htmlColorsToRGB[hue].split(',');
  return getContrastColor({ r, g, b })
}

